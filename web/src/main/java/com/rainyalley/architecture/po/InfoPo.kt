package com.rainyalley.architecture.po


class InfoPo {

    var id: String? = null
        set(id) {
            field = this.id
        }

    var name: String? = null
        set(name) {
            field = this.name
        }

    var uri: String? = null
        set(uri) {
            field = this.uri
        }

    var text: Int = 0
        set(text) {
            field = this.text
        }
}